package boot.spring.pagemodel;

public class MSG {
	String msg;
	Object obj;
	
	public MSG() {
		super();
	}
	public MSG(String msg) {
		super();
		this.msg = msg;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public Object getObj() {
		return obj;
	}
	public void setObj(Object obj) {
		this.obj = obj;
	}
	
}
